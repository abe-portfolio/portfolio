// H, W
// S(1,1) S(1,2) ... S(1,W)
// S(2,1) S(2,2) ... S(2,W)
// S(3,1) S(3,2) ... S(3,W)
// ...
// S(H,1) S(H,2) ... S(H,W)

use proconio::input;
use proconio::marker::Chars;
// use proconio::{input, marker::Chars};  とすれば１行で記述可能

fn main() {
    input! {
        h: usize,
        w: usize,
        s: [Chars; h],  // h 行分の入力を受け取る
    };
}

