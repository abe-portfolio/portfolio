// n
// v1 v2 ... vn
use proconio::input;

fn input2() {
    input! {
        n: usize, // nはvを受け取る際のインデックスとして使用する。このような場合やfor文などでインデックスとして使用される場合はusizeで受け取らないとコンパイルエラーになる
        v: [i32; n],
    };
}

