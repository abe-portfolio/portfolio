<?php
// ========================================
// 曜日判定（週末かどうか）
// ========================================

$day = 3; // 1=月曜, 2=火曜, ..., 7=日曜

// ★ switch文での実装
switch ($day) {
    case 1:
    case 2:
    case 3:
    case 4:
    case 5:
        echo "平日です。仕事の日！\n";
        break;
    case 6:
    case 7:
        echo "週末です。休みの日！\n";
        break;
    default:
        echo "無効な曜日です。\n";
        break;
}


// ------------------------------------------------------------
// ★ if文での実装（同じ条件）
if ($day >= 1 && $day <= 5) {
    echo "平日です。仕事の日！\n";
} elseif ($day === 6 || $day === 7) {
    echo "週末です。休みの日！\n";
} else {
    echo "無効な曜日です。\n";
}


// ------------------------------------------------------------
// ★ match式での実装（PHP 8.0以降）
$message = match ($day) {
    1, 2, 3, 4, 5 => "平日です。仕事の日！",
    6, 7 => "週末です。休みの日！",
    default => "無効な曜日です。"
};
echo $message . "\n";
?>